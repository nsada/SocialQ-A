package domain;

public class Friend {
	private int A;
	private int B;
	private int openB;
	public int getA() {
		return A;
	}
	public void setA(int a) {
		A = a;
	}
	public int getB() {
		return B;
	}
	public void setB(int b) {
		B = b;
	}
	public int getOpenB() {
		return openB;
	}
	public void setOpenB(int openB) {
		this.openB = openB;
	}
	
	
}
